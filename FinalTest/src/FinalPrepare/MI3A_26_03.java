    public void loadData() throws IOException{
        try(BufferedReader br = new BufferedReader(new FileReader("D:/Data/test.csv"))){
            String line;
            while((line=br.readLine()) != null){
                if(isTitle){
                    isTitle = false;
                }
                else if(!(isTitle)){
                    dtm.addRow(line.split(","));
                }  
            }
        }
    }
    
