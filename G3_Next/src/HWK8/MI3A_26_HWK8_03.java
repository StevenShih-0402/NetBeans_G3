    void showFileContent(String path) throws FileNotFoundException, IOException{
        try(BufferedReader br = new BufferedReader(new FileReader(path))){
            String line, content = "";
            while((line=br.readLine()) != null){
                content += line+"\n";
            }
            fileText.setText(content);
        }
    }
    
    void writeBackToFile() throws IOException{
        JFrame Block = new JFrame();
        
        try(BufferedWriter bw = new BufferedWriter(new FileWriter(filePath.getText()))){
            bw.write(fileText.getText());
            JOptionPane.showMessageDialog(Block, "在這份文件，你留下了一道新的傷痕......");
        }
    }
    
    void buildFile(String path) throws IOException{
        File newFile = new File(path);
        newFile.createNewFile();
        JFrame Block = new JFrame();
        
        try(BufferedWriter bw = new BufferedWriter(new FileWriter(path))){
            bw.write(fileText.getText());
            JOptionPane.showMessageDialog(Block, "新的歷史誕生了......");
        }
    }
    
