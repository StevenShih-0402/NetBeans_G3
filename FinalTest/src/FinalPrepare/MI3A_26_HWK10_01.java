    public void initMenu() throws IOException{
        try (BufferedReader br = new BufferedReader(new FileReader(path))) {
            String line;
            int count = 0;
            while ((line = br.readLine()) != null) {
                moneys[count++] = line.split(",")[1];
                dlm1.addElement(line.split(",")[0]);
            }
        }
    }

