    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        int[] indices = menu.getSelectedIndices();
        for (int i : indices) {
            int onceMoney = Integer.parseInt(moneys[i])*Integer.parseInt(count.getText());
            dlm2.addElement(dlm1.getElementAt(i) + "->" + onceMoney);
            total += onceMoney;
        }
        money.setText(String.valueOf(total));
    }//GEN-LAST:event_addActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // TODO add your handling code here:
        int[] indices = list.getSelectedIndices();
        for (int i : reverse(indices)) {
            total -= Integer.parseInt(dlm2.getElementAt(i).toString().split("->")[1]);
            dlm2.removeElementAt(i);
        }
        money.setText(String.valueOf(total));
    }//GEN-LAST:event_delActionPerformed

    public int[] reverse(int[] indices) {
        int[] newItem = new int[indices.length];
        for (int i = 0; i < newItem.length; i++) {
            newItem[i] = indices[indices.length - 1 - i];
        }
        
        return newItem;
    }
